/* L1Audio_Msg_AFE_Switch */
void L1TRC_Send_L1Audio_Msg_AFE_Switch(unsigned char v1, unsigned char v2)
{
	TRC_START_FILL_TASK(5);
	TRC_MAKE_CHAR_0c(TRC_TASK_BUFFER, TRC_LOCAL, 0xF8);
	TRC_MAKE_CHAR_1c(TRC_TASK_BUFFER, TRC_LOCAL, 0xC0);
	TRC_MAKE_CHAR_2c(TRC_TASK_BUFFER, TRC_LOCAL, 0x20);
	TRC_MAKE_CHAR_3c(TRC_TASK_BUFFER, TRC_LOCAL, v1);
	TRC_MAKE_CHAR_0c(TRC_TASK_BUFFER, TRC_LOCAL, v2);
	TRC_PAD_FN(0xFAFAFA);
	TRC_END_FILL_TASK_3w;
}

